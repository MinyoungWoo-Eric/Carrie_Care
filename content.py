def items_for_date(d: date):
    """해당 날짜에 표시해야 하는 체크 항목 목록을 반환."""
    weekday = d.weekday()
    return [
        item
        for item in CHECKLIST_ITEMS
        if item["days"] is None or weekday in item["days"]
    ]


def items_by_role(items):
    """항목 리스트를 역할별 dict 로 그룹화 (표시 순서는 ROLES 기준)."""
    grouped = {role: [] for role, _ in ROLES}
    for item in items:
        grouped[item["role"]].append(item)
    return grouped


